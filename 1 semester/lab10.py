#Цель: проектирование алгоритма и программы циклической структуры с предусловием функции f(x)=3*sin(x/3)+2*cos(x/2)
#Переменные:
#x0 (float) – вводимое начальное значение х0
#xn (float) – вводимое конечное значение xn
#hx (float) – вводимое значение шага функции
#x (float) – промежуточные x между значениями x0 и xn (x=x0(hx)xn)
#ymin (float) - минимальное значение функции на отрезке x0(hx)xn
#ymax (float) - максимальное значение функции на отрезке x0(hx)xn
#Метод: поиск минимального и максимального значений функции на заданном отрезке
#методом сравнения значения функции в каждой точке с предыдущим (заданным) максимумом, минимумом
