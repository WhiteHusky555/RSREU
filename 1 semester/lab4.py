from math import sqrt #Импортируем sqrt из модуля math
# Цель: вычисление значений функций, введя функцию пользователя
# Переменная x - вводится с клавиатуры
# Метод: ввод пользовательской функции, ввод переменной, вычисление значения выражения с её помощью
# Программист: Непрокин И.С.
