a=float(input('Введите длину стороны а: '))
b=float(input('Введите длину стороны b: '))
c=float(input('Введите длину стороны c: '))
if (a+b)>c and (b+c)>a and (a+c)>b and a>0 and b>0 and c>0:
    if (a*a+b*b)==(c*c) or (a*a+c*c)==(b*b) or (b*b+c*c)==(a*a):
        print('Треугольник с заданными сторонами - прямоугольный')
    else:
        if (a*a+b*b+c*c-max(a, b, c)*max(a, b, c))>(max(a, b, c)*max(a, b, c)):
            print('Треугольник с заданными сторонами - остроугольный')
        else:
            print('Треугольник с заданными сторонами - тупоугольный')
else:
    print('Треугольника с заданными сторонами не существует')

