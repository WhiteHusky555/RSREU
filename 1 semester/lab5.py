# Цель: Определить вид треугольника, зная длины его сторон, используя оператор условного перехода.
# Переменные:
# a(float) – длина стороны а
# b(float) – длина стороны b
# c(float) – длина стороны c
# Методы: Проверка существования треугольника с заданными сторонами (Чтобы треугольник существовал,
# сумма двух сторон треугольника всегда должна быть больше третей стороны. a + b > c, b + c > a, a + c > b).
# Проверка треугольника на факт бытия прямоугольным (a^2+b^2=c^2  или a^2+c^2=b^2  или b^2+c^2=a^2).
# Проверка треугольника на факт бытия остроугольным (a^2+b^2>c^2  или a^2+c^2>b^2  или b^2+c^2>a^2).
# Программист: Непрокин И.С.
